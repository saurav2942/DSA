#include<bits/stdc++.h>
using namespace std;

int main() {
    // Or Operator '|'
    int a = 5 | 6; // 101 | 110 -> 111
    cout << a << "\n";
    // And Operator '&'
    a = 5 & 6; // 101 & 110 -> 100
    cout << a << "\n";
    // XOR Operator '^'
    a = 5 ^ 6; // 101 ^ 110 -> 011
    cout << a << "\n";
    return 0;
}

/*
Output -
7
4
3
*/